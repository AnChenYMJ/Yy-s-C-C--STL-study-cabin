#include <stdio.h>
#include <stdlib.h>

int main(void)
{
	int b[3] = { 4, 8, 9 },
		c[5] = { 6, 5, 1, 3, 4 },
		d[2] = { 7, 2 },
		e[4] = { 5, 4, 7, 9 },
		f[4] = { 6, 4, 2, 1 };

	int* a[5] = { &b[0], c, d, e, f }; //可以用数组的地址的不同表现形式来表达

	printf("%d, %d \n", a[3][2], a[4][3]);
	//下标运算：地址+常量， 而这里的拉链结构满足下标运算a[3]为地址+2，
	//访问格式与意义与二维数组相同，但本质（内存结构）是不同的
	//二维数组中地址是相邻的排序，而这里的数组元素排序不是相邻排序的
	//元素类型一定要相同


	system("pause");
	return 0;
}